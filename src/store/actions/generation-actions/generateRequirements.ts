import { flow, toGenerator } from "mobx-state-tree";
import { Iteration, Store } from "store";
import ai from "store/api";

import {
  generatePrompt,
  generator,
  prepareContent,
  systemPrompt,
} from "./utilities";

const generateRequirements = flow(function* (self: Store) {
  // Generate requirements
  const result = yield* toGenerator(
    ai.createChatCompletion({
      model: "gpt-3.5-turbo",
      n: 1,
      temperature: 0,
      messages: [
        { role: "user", content: systemPrompt },
        {
          role: "user",
          content: `description: ${self.description}`,
        },
        {
          role: "user",
          content: `product overview: ${self.productOverview}`,
        },
        {
          role: "user",
          content: `user stories: ${self.userStories
            .map(({ content }) => `- ${content}`)
            .join("\n")}`,
        },
        ...generatePrompt("requirements"),
      ],
    })
  );
  const requirements = result.data.choices[0].message?.content;

  if (requirements == null) {
    throw new Error("No response generated by ai for requirements!");
  }

  self.setRequirements(prepareContent(requirements));
  self.eventTarget.emit("iterationUpdate", Iteration.requirements);
}) as (self_: unknown) => Promise<void>;

export default generator(generateRequirements);
